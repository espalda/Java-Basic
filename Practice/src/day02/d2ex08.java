package day02;

public class d2ex08 {

	public static void main(String[] args) {
		// 반복문 for : 규칙성을 찾아야 함
		// for(초기화 ; 조건식 ; 증감연산) {
		//		실행문; } 실행문이 한줄이면 중괄호 생략 가능
		// i는 반복문에서 사용할 변수
		int i = 0; 
		for(i=1; i<=5; i++){
			System.out.println("Hello World!");
		}
		// i++대신 i=i+1 또는 i+=1  사용 가능
	}

}
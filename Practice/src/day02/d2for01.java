package day02;

public class d2for01 {

	public static void main(String[] args) {
		/* d2 반복문 for
		 * 반복문 for : 규칙성을 찾아야 함
		 * for(초기화 ; 조건식 ; 증감식) {
		 * 		실행문;} 실행문이 한줄이면 중괄호 생략 가능
		 * i는 반복문에서 사용할 변수
		 */
		
		int i = 0; 
		for(i=1; i<=5; i++){	//i++대신 i=i+1 또는 i+=1  사용 가능
			System.out.println("Hello World!");
		}
		
	}

}
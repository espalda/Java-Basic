package day10;

public class d10override {
	public static void main(String[] args) {
		/* d10 오버라이딩 overriding
		 * 부모클래스에서 물려받은 메서드를 재정의 하는것
		 * 부모클래스의 선언부(리턴타입,메서드명,매개변수)가 같아야 한다
		 * 오버로딩 overload 은 기존에 없던 새로운 메서드를 동일한 메서드 명으로 여러개 만드는것
		 * 오버라이딩 override 는 상속받은 메서드를 변경하는것
		 * super() : 부모클래스의 생성자 호출
		 * 
		 * static : 클래스 멤버변수나 클래스 멤버 메서드를 생성할 때 사용
		 * 클래스 멤버들은 클래스를 통해 사용한다,
		 * 객체를 생성하지 않고 사용
		 * 하지만 객체를 통해 사용할수 있는다
		 */
	}
}
